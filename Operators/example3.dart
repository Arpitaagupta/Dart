void main(List<String> args) {

  //Unary Postfix Operators
  var age = 40;
  print('age = $age');
  print('____________________________________________________________________________________________________________');
  print(age--);  //it prints age then it subtracts -1 from value of age
  print('Value of age after age-- is ');   //age - 1
  print(age);
  print('____________________________________________________________________________________________________________');
  print(age++);  //it prints age then it adds +1 to the value of age
   print('Value of age after age++ is ');   //age + 1
  print(age);

}